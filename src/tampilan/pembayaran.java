    protected final void datatable() {
        Object[] Baris = {"Id Warga","Nama","Tanggal","Bayar"};
        tableModel = new DefaultTableModel(null, Baris);
        table_bayar.setModel(tableModel);
        String sql = "select * from bayar";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String txt_id = hasil.getString("id_warga");
                String txt_nama = hasil.getString("nama");
                Date dctglbyr = hasil.getDate("tanggal_bayar");
                String jtotal = hasil.getString("bayar");
                
                
                String[] data = {txt_id,txt_nama,String.valueOf(dctglbyr),jtotal};
                tableModel.addRow(data);

            }
        } catch (Exception e) {
        }
    }


    private void table_bayarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_bayarMouseClicked
        // TODO add your handling code here:
         int bar = table_bayar.getSelectedRow();
        String a = tableModel.getValueAt(bar, 0).toString();
        String b = tableModel.getValueAt(bar, 1).toString();
        String c = tableModel.getValueAt(bar, 2).toString();
        String d = tableModel.getValueAt(bar, 3).toString();
        
        
        txt_id.setText(a);
        txt_nama.setText(b);
         java.util.Date date = null;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse(c);
        } catch (ParseException ex) {
            Logger.getLogger(pembayaran.class.getName()).log(Level.SEVERE, null, ex);
        }
        dctglbyr.setDate(date);
       
        txt_bayar.setText(d);
    }//GEN-LAST:event_table_bayarMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         String sql = "insert into bayar values (?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txt_id.getText());
            stat.setString(2, txt_nama.getText());
            java.sql.Date tglDate = new java.sql.Date(dctglbyr.getDate().getTime());
            stat.setDate(3, tglDate);
            stat.setString(4, txt_bayar.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            txt_id.requestFocus();
            datatable();
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan " + e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


         protected void datatable() {
        Object[] Baris = {"Id Audit","Tanggal","Pemasukan","Pengeluaran","Total"};
        tableModel = new DefaultTableModel(null, Baris);
        tableaudit.setModel(tableModel);
        String sql = "select * from audit";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String id_audit = hasil.getString("id_audit");
                Date tanggal = hasil.getDate("tanggal");
                String total_pemasukan = hasil.getString("total_pemasukan");
                String anggaran_pengeluaran = hasil.getString("anggaran_pengeluaran");
                String total = hasil.getString("total");
              
                String[] data = {id_audit, String.valueOf(tanggal), total_pemasukan, anggaran_pengeluaran,total};
                tableModel.addRow(data);

            }
        } catch (Exception e) {
        }
    }

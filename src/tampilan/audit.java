    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
         try {
            String sql = "Update audit set tanggal=?,total_pemasukan=?,anggaran_pengeluaran=?,total=?"
                    + " where id_audit='" + id + "'";
            PreparedStatement stat = conn.prepareStatement(sql);
            java.sql.Date tglDate = new java.sql.Date(dctanggal.getDate().getTime());
            stat.setDate(1, tglDate);
            stat.setString(2, txtbayarkas.getText());
            stat.setString(3, txtpengeluaran.getText());
            stat.setString(4, lbltotal.getText());            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            kosong();
            dctanggal.requestFocus();
            datatable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

